public class PrimitiveDataTypes {
    public static void main(String[] args) {
        // Primitive data types in Java
        byte byteValue = 100; // 8-bit signed integer
        short shortValue = 10000; // 16-bit signed integer
        int intValue = 100000; // 32-bit signed integer
        long longValue = 100000L; // 64-bit signed integer
        float floatValue = 10.5f; // Single-precision 32-bit 
        double doubleValue = 20.99; // Double-precision 64-bit 
        char charValue = 'A'; // Single 16-bit Unicode character
        boolean booleanValue = true; // true or false 1-bit value

        System.out.println("Byte value: " + byteValue);
        System.out.println("Short value: " + shortValue);
        System.out.println("Int value: " + intValue);
        System.out.println("Long value: " + longValue);
        System.out.println("Float value: " + floatValue);
        System.out.println("Double value: " + doubleValue);
        System.out.println("Char value: " + charValue);
        System.out.println("Boolean value: " + booleanValue);
    }
}
